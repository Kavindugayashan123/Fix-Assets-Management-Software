     public  void geAssetNo()
    {
        db.connection();
        ResultSet rs;
        String sql = "SELECT * FROM direct_purchased ORDER BY AssetNo DESC LIMIT 1";
        rs = db.selectQuery(sql);
        try {
            while(rs.next())
            {
                String a = (rs.getString(1));
                int value = Integer.parseInt(a.replaceAll("[^0-9]", ""));
                value = value + 1;
                String res = "D" + value;
              txtAssetNo.setText(res);
               
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"SQL ERROR! " + ex.getMessage());   
        }
                
    }
