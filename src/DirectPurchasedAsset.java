    private void btnAddAssetListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddAssetListActionPerformed
        db.connection();
        
        if(txtAssetName.getText().equals("")){
        JOptionPane.showMessageDialog(null,"Please enter Asset Name ");
          }
        
        else if(txtPrice.getText().equals("")){
        JOptionPane.showMessageDialog(null,"Please enter Price");
          }
         else if(txtLifeTime.getText().equals("")){
        JOptionPane.showMessageDialog(null,"Please enter Life Time");
          }
        
         else if(txtPurchasedDate.getDate().equals("")){
        JOptionPane.showMessageDialog(null,"Please choose a Date");
          }
         else if(Integer.parseInt(txtLifeTime.getText())<0.0){
           JOptionPane.showMessageDialog(null,"Life Time should be Positive number");
         }
         else if(((txtPrice.getText().trim().matches("[a-zA-z,]+"))))
          {  JOptionPane.showMessageDialog(null,"Price should only contains numbers ");} 
         
         else{
         SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
       String date = sdf.format(txtPurchasedDate.getDate());
       
        String assetNo = txtAssetNo.getText();
        String assetName = txtAssetName.getText();
        String category = (String) cmbCategory.getSelectedItem();
        String price = txtPrice.getText();
        String DateOfPurch = date;
        String lifeTime = txtLifeTime.getText();
        
        String sql = String.format("INSERT INTO direct_purchased(AssetNo,AssetName,category,price,purchased_date,Life_Time)VALUES('%s','%s','%s','%s','%s','%s')"
                ,assetNo,assetName,category,price,DateOfPurch,lifeTime);
     
        int i = db.updateQuery(sql);
        if(i>0)
        { JOptionPane.showMessageDialog(null,"successfully added");
           txtAssetName.setText(null);
           txtPrice.setText(null);
           txtPurchasedDate.setDate(null);
           txtLifeTime.setText(null);
        }
        else
        { JOptionPane.showMessageDialog(null,"Not entered");
        }
        
         }
    }//GEN-LAST:event_btnAddAssetListActionPerformed

